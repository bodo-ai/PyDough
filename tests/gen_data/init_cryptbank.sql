-- Custom SQL schema to initialize a custom CRYPTBANK database with tables for
-- customers, bank accounts, branches, and transactions. The columns are
-- "encrypted" using faux masking schemes with masking/unmasking protocols set
-- in the metadata.

CREATE TABLE CUSTOMERS (
  c_key INTEGER NOT NULL PRIMARY KEY, -- ENCRYPTED: arithmetic shift
  c_fname VARCHAR, -- ENCRYPTED: uppercase
  c_lname VARCHAR, -- ENCRYPTED: uppercase
  c_phone VARCHAR, -- xxx-xxx-xxxx -> ENCRYPTED: digit-switch
  c_email VARCHAR, -- prefix@domain.type -> ENCRYPTED: first-char
  c_addr VARCHAR, -- street address;city;state;zip -> ENCRYPTED: first-char
  c_birthday VARCHAR -- date -> ENCRYPTED: day-shift
);

CREATE TABLE BRANCHES (
  b_key INTEGER NOT NULL PRIMARY KEY,
  b_name VARCHAR,
  b_addr VARCHAR -- street address;city;state;zip
);

CREATE TABLE ACCOUNTS (
  a_key INTEGER NOT NULL PRIMARY KEY, -- ENCRYPTED: INTEGER CONCATENATION
  a_custkey INTEGER NOT NULL,
  a_branchkey INTEGER NOT NULL,
  a_balance REAL, --> ENCRYPTED: geometric shift
  a_type VARCHAR, --> ENCRYPTED: first-char
  a_open_ts VARCHAR -- timestamp -> ENCRYPTED: second-shift
);

CREATE TABLE TRANSACTIONS (
  t_key INTEGER NOT NULL PRIMARY KEY,
  t_sourceaccount INTEGER NOT NULL,
  t_destaccount INTEGER NOT NULL,
  t_amount REAL, --> ENCRYPTED: arithmetic shift
  t_ts VARCHAR -- timestamp -> ENCRYPTED: second-shift
);

