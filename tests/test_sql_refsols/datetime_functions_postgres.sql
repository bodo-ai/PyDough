SELECT
  CURRENT_TIMESTAMP AS ts_now_1,
  DATE_TRUNC('DAY', CURRENT_TIMESTAMP) AS ts_now_2,
  DATE_TRUNC('MONTH', CURRENT_TIMESTAMP) AS ts_now_3,
  CURRENT_TIMESTAMP + INTERVAL '1 HOUR' AS ts_now_4,
  DATE_TRUNC('MONTH', CAST('2025-01-01 00:00:00' AS TIMESTAMP)) AS ts_now_5,
  CAST('1995-10-12 00:00:00' AS TIMESTAMP) AS ts_now_6,
  EXTRACT(YEAR FROM CAST(o_orderdate AS TIMESTAMP)) AS year_col,
  EXTRACT(YEAR FROM CAST('2020-05-01 00:00:00' AS TIMESTAMP)) AS year_py,
  EXTRACT(YEAR FROM CAST('1995-10-10 00:00:00' AS TIMESTAMP)) AS year_pd,
  EXTRACT(MONTH FROM CAST(o_orderdate AS TIMESTAMP)) AS month_col,
  EXTRACT(MONTH FROM CAST('2025-02-25' AS TIMESTAMP)) AS month_str,
  EXTRACT(MONTH FROM CAST('1992-01-01 12:30:45' AS TIMESTAMP)) AS month_dt,
  EXTRACT(DAY FROM CAST(o_orderdate AS TIMESTAMP)) AS day_col,
  EXTRACT(DAY FROM CAST('1996-11-25 10:45:00' AS TIMESTAMP)) AS day_str,
  EXTRACT(HOUR FROM CAST('1995-12-01 23:59:59' AS TIMESTAMP)) AS hour_str,
  EXTRACT(MINUTE FROM CAST('1995-12-01 23:59:59' AS TIMESTAMP)) AS minute_str,
  EXTRACT(SECOND FROM CAST('1992-01-01 00:00:59' AS TIMESTAMP)) AS second_ts,
  EXTRACT(EPOCH FROM CAST('1992-01-01' AS TIMESTAMP) - CAST(o_orderdate AS TIMESTAMP)) / 86400 AS dd_col_str,
  EXTRACT(EPOCH FROM CAST(o_orderdate AS TIMESTAMP) - CAST('1992-01-01' AS TIMESTAMP)) / 86400 AS dd_str_col,
  CAST(EXTRACT(YEAR FROM AGE(CAST(o_orderdate AS TIMESTAMP), CAST('1995-10-10 00:00:00' AS TIMESTAMP))) * 12 + EXTRACT(MONTH FROM AGE(CAST(o_orderdate AS TIMESTAMP), CAST('1995-10-10 00:00:00' AS TIMESTAMP))) AS BIGINT) AS dd_pd_col,
  CAST(EXTRACT(YEAR FROM AGE(CAST('1992-01-01 12:30:45' AS TIMESTAMP), CAST(o_orderdate AS TIMESTAMP))) AS BIGINT) AS dd_col_dt,
  EXTRACT(DAYS FROM (
    CAST('1992-01-01 12:30:45' AS TIMESTAMP) - CAST('1992-01-01' AS TIMESTAMP)
  )) / 7 AS dd_dt_str,
  EXTRACT(DOW FROM CAST(o_orderdate AS TIMESTAMP)) AS dow_col,
  EXTRACT(DOW FROM CAST('1992-07-01' AS TIMESTAMP)) AS dow_str,
  EXTRACT(DOW FROM CAST('1992-01-01 12:30:45' AS TIMESTAMP)) AS dow_dt,
  EXTRACT(DOW FROM CAST('1995-10-10 00:00:00' AS TIMESTAMP)) AS dow_pd,
  CASE
    WHEN EXTRACT(DOW FROM CAST(o_orderdate AS TIMESTAMP)) = 0
    THEN 'Sunday'
    WHEN EXTRACT(DOW FROM CAST(o_orderdate AS TIMESTAMP)) = 1
    THEN 'Monday'
    WHEN EXTRACT(DOW FROM CAST(o_orderdate AS TIMESTAMP)) = 2
    THEN 'Tuesday'
    WHEN EXTRACT(DOW FROM CAST(o_orderdate AS TIMESTAMP)) = 3
    THEN 'Wednesday'
    WHEN EXTRACT(DOW FROM CAST(o_orderdate AS TIMESTAMP)) = 4
    THEN 'Thursday'
    WHEN EXTRACT(DOW FROM CAST(o_orderdate AS TIMESTAMP)) = 5
    THEN 'Friday'
    WHEN EXTRACT(DOW FROM CAST(o_orderdate AS TIMESTAMP)) = 6
    THEN 'Saturday'
  END AS dayname_col,
  CASE
    WHEN EXTRACT(DOW FROM CAST('1995-06-30' AS TIMESTAMP)) = 0
    THEN 'Sunday'
    WHEN EXTRACT(DOW FROM CAST('1995-06-30' AS TIMESTAMP)) = 1
    THEN 'Monday'
    WHEN EXTRACT(DOW FROM CAST('1995-06-30' AS TIMESTAMP)) = 2
    THEN 'Tuesday'
    WHEN EXTRACT(DOW FROM CAST('1995-06-30' AS TIMESTAMP)) = 3
    THEN 'Wednesday'
    WHEN EXTRACT(DOW FROM CAST('1995-06-30' AS TIMESTAMP)) = 4
    THEN 'Thursday'
    WHEN EXTRACT(DOW FROM CAST('1995-06-30' AS TIMESTAMP)) = 5
    THEN 'Friday'
    WHEN EXTRACT(DOW FROM CAST('1995-06-30' AS TIMESTAMP)) = 6
    THEN 'Saturday'
  END AS dayname_str,
  CASE
    WHEN EXTRACT(DOW FROM CAST('1993-08-15 00:00:00' AS TIMESTAMP)) = 0
    THEN 'Sunday'
    WHEN EXTRACT(DOW FROM CAST('1993-08-15 00:00:00' AS TIMESTAMP)) = 1
    THEN 'Monday'
    WHEN EXTRACT(DOW FROM CAST('1993-08-15 00:00:00' AS TIMESTAMP)) = 2
    THEN 'Tuesday'
    WHEN EXTRACT(DOW FROM CAST('1993-08-15 00:00:00' AS TIMESTAMP)) = 3
    THEN 'Wednesday'
    WHEN EXTRACT(DOW FROM CAST('1993-08-15 00:00:00' AS TIMESTAMP)) = 4
    THEN 'Thursday'
    WHEN EXTRACT(DOW FROM CAST('1993-08-15 00:00:00' AS TIMESTAMP)) = 5
    THEN 'Friday'
    WHEN EXTRACT(DOW FROM CAST('1993-08-15 00:00:00' AS TIMESTAMP)) = 6
    THEN 'Saturday'
  END AS dayname_dt
FROM tpch.orders
