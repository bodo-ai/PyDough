WITH _T0 AS (
  SELECT
    ANY_VALUE(SEARCHES.search_user_id) AS ANYTHING_SEARCH_USER_ID
  FROM SEARCHES AS SEARCHES
  JOIN EVENTS AS EVENTS
    ON LOWER(SEARCHES.search_string) LIKE CONCAT('%', LOWER(EVENTS.ev_name), '%')
  JOIN ERAS AS ERAS
    ON ERAS.er_end_year > DATE_PART(YEAR, CAST(EVENTS.ev_dt AS DATETIME))
    AND ERAS.er_name = 'Cold War'
    AND ERAS.er_start_year <= DATE_PART(YEAR, CAST(EVENTS.ev_dt AS DATETIME))
  GROUP BY
    SEARCHES.search_id
), _S5 AS (
  SELECT
    COUNT(*) AS N_COLD_WAR_SEARCHES,
    ANYTHING_SEARCH_USER_ID
  FROM _T0
  GROUP BY
    ANYTHING_SEARCH_USER_ID
)
SELECT
  USERS.user_name,
  _S5.N_COLD_WAR_SEARCHES AS n_cold_war_searches
FROM USERS AS USERS
JOIN _S5 AS _S5
  ON USERS.user_id = _S5.ANYTHING_SEARCH_USER_ID
ORDER BY
  N_COLD_WAR_SEARCHES DESC NULLS LAST,
  USER_NAME NULLS FIRST
LIMIT 3
