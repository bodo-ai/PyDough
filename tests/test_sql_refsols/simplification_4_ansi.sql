WITH _t1 AS (
  SELECT
    sbtxdatetime
  FROM main.sbtransaction
  WHERE
    EXTRACT(YEAR FROM CAST(sbtxdatetime AS DATETIME)) = 2023
  QUALIFY
    ROW_NUMBER() OVER (ORDER BY sbtxdatetime DESC NULLS FIRST) = 1
    OR ROW_NUMBER() OVER (ORDER BY sbtxdatetime NULLS LAST) = 1
)
