SELECT
  sbtxdatetime AS x,
  CAST('2025-05-02 11:00:00' AS TIMESTAMP) AS y1,
  CAST('2023-04-03 13:16:30' AS TIMESTAMP) AS y,
  DATEDIFF(YEAR, sbtxdatetime, CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS years_diff,
  DATEDIFF(MONTH, sbtxdatetime, CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS months_diff,
  DATEDIFF(DAY, sbtxdatetime, CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS days_diff,
  DATEDIFF(HOUR, sbtxdatetime, CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS hours_diff,
  DATEDIFF(MINUTE, sbtxdatetime, CAST('2023-04-03 13:16:30' AS TIMESTAMP)) AS minutes_diff,
  DATEDIFF(SECOND, sbtxdatetime, CAST('2023-04-03 13:16:30' AS TIMESTAMP)) AS seconds_diff
FROM MAIN.SBTRANSACTION
WHERE
  YEAR(sbtxdatetime) < 2025
ORDER BY
  YEARS_DIFF NULLS FIRST
LIMIT 30
