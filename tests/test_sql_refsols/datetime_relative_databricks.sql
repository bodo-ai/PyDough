WITH _t0 AS (
  SELECT
    o_orderdate
  FROM tpch.orders
  ORDER BY
    o_custkey,
    1
  LIMIT 10
)
SELECT
  TRUNC(CAST(o_orderdate AS TIMESTAMP), 'YEAR') AS d1,
  TRUNC(CAST(o_orderdate AS TIMESTAMP), 'MONTH') AS d2,
  DATEADD(DAY, -7, DATEADD(MONTH, 9, ADD_MONTHS(CAST(o_orderdate AS TIMESTAMP), -132))) + INTERVAL '5' HOUR - INTERVAL '3' MINUTE + INTERVAL '1' SECOND AS d3,
  CAST('2025-07-04 12:00:00' AS TIMESTAMP) AS d4,
  CAST('2025-07-04 12:58:00' AS TIMESTAMP) AS d5,
  CAST('2025-07-26 02:45:25' AS TIMESTAMP) AS d6
FROM _t0
ORDER BY
  o_orderdate
