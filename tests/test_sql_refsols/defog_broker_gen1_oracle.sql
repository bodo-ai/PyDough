SELECT
  MIN(SBDAILYPRICE.sbdpclose) AS lowest_price
FROM MAIN.SBDAILYPRICE SBDAILYPRICE
JOIN MAIN.SBTICKER SBTICKER
  ON SBDAILYPRICE.sbdptickerid = SBTICKER.sbtickerid
  AND SBTICKER.sbtickersymbol = 'VTI'
WHERE
  (
    TRUNC(CAST(CAST(SYS_EXTRACT_UTC(SYSTIMESTAMP) AS DATE) AS DATE), 'DD') - TRUNC(CAST(CAST(SBDAILYPRICE.sbdpdate AS DATE) AS DATE), 'DD')
  ) <= 7
