SELECT
  sbtxdatetime AS x,
  TIME_STR_TO_TIME('2025-05-02 11:00:00') AS y1,
  TIME_STR_TO_TIME('2023-04-03 13:16:30') AS y,
  DATEDIFF(TIME_STR_TO_TIME('2025-05-02 11:00:00'), CAST(sbtxdatetime AS DATETIME), YEAR) AS years_diff,
  DATEDIFF(TIME_STR_TO_TIME('2025-05-02 11:00:00'), CAST(sbtxdatetime AS DATETIME), MONTH) AS months_diff,
  DATEDIFF(TIME_STR_TO_TIME('2025-05-02 11:00:00'), CAST(sbtxdatetime AS DATETIME), DAY) AS days_diff,
  DATEDIFF(TIME_STR_TO_TIME('2025-05-02 11:00:00'), CAST(sbtxdatetime AS DATETIME), HOUR) AS hours_diff,
  DATEDIFF(TIME_STR_TO_TIME('2023-04-03 13:16:30'), CAST(sbtxdatetime AS DATETIME), MINUTE) AS minutes_diff,
  DATEDIFF(TIME_STR_TO_TIME('2023-04-03 13:16:30'), CAST(sbtxdatetime AS DATETIME), SECOND) AS seconds_diff
FROM MAIN.SBTRANSACTION
WHERE
  EXTRACT(YEAR FROM CAST(sbtxdatetime AS DATETIME)) < 2025
ORDER BY
  4 NULLS FIRST
FETCH FIRST 30 ROWS ONLY
