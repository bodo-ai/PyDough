SELECT
  sbtxdatetime AS x,
  CAST('2025-05-02 11:00:00' AS TIMESTAMP) AS y1,
  CAST('2023-04-03 13:16:30' AS TIMESTAMP) AS y,
  DATEDIFF(YEAR, CAST(sbtxdatetime AS DATETIME), CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS years_diff,
  DATEDIFF(MONTH, CAST(sbtxdatetime AS DATETIME), CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS months_diff,
  DATEDIFF(DAY, CAST(sbtxdatetime AS DATETIME), CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS days_diff,
  DATEDIFF(HOUR, CAST(sbtxdatetime AS DATETIME), CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS hours_diff,
  DATEDIFF(MINUTE, CAST(sbtxdatetime AS DATETIME), CAST('2023-04-03 13:16:30' AS TIMESTAMP)) AS minutes_diff,
  DATEDIFF(SECOND, CAST(sbtxdatetime AS DATETIME), CAST('2023-04-03 13:16:30' AS TIMESTAMP)) AS seconds_diff
FROM MAIN.SBTRANSACTION
WHERE
  YEAR(CAST(sbtxdatetime AS TIMESTAMP)) < 2025
ORDER BY
  4 NULLS FIRST
LIMIT 30
