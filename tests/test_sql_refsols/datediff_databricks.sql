SELECT
  sbtxdatetime AS x,
  CAST('2025-05-02 11:00:00' AS TIMESTAMP) AS y1,
  CAST('2023-04-03 13:16:30' AS TIMESTAMP) AS y,
  YEAR(TO_DATE(CAST('2025-05-02 11:00:00' AS TIMESTAMP))) - YEAR(TO_DATE(sbtxdatetime)) AS years_diff,
  (
    YEAR(TO_DATE(CAST('2025-05-02 11:00:00' AS TIMESTAMP))) - YEAR(TO_DATE(sbtxdatetime))
  ) * 12 + MONTH(TO_DATE(CAST('2025-05-02 11:00:00' AS TIMESTAMP))) - MONTH(TO_DATE(sbtxdatetime)) AS months_diff,
  DATEDIFF(
    DAY,
    CAST(sbtxdatetime AS DATE),
    CAST(CAST('2025-05-02 11:00:00' AS TIMESTAMP) AS DATE)
  ) AS days_diff,
  DATEDIFF(
    DAY,
    CAST(sbtxdatetime AS DATE),
    CAST(CAST('2025-05-02 11:00:00' AS TIMESTAMP) AS DATE)
  ) * 24 + EXTRACT(HOUR FROM CAST('2025-05-02 11:00:00' AS TIMESTAMP)) - EXTRACT(HOUR FROM CAST(sbtxdatetime AS TIMESTAMP)) AS hours_diff,
  (
    DATEDIFF(
      DAY,
      CAST(sbtxdatetime AS DATE),
      CAST(CAST('2023-04-03 13:16:30' AS TIMESTAMP) AS DATE)
    ) * 24 + EXTRACT(HOUR FROM CAST('2023-04-03 13:16:30' AS TIMESTAMP)) - EXTRACT(HOUR FROM CAST(sbtxdatetime AS TIMESTAMP))
  ) * 60 + EXTRACT(MINUTE FROM CAST('2023-04-03 13:16:30' AS TIMESTAMP)) - EXTRACT(MINUTE FROM CAST(sbtxdatetime AS TIMESTAMP)) AS minutes_diff,
  (
    (
      DATEDIFF(
        DAY,
        CAST(sbtxdatetime AS DATE),
        CAST(CAST('2023-04-03 13:16:30' AS TIMESTAMP) AS DATE)
      ) * 24 + EXTRACT(HOUR FROM CAST('2023-04-03 13:16:30' AS TIMESTAMP)) - EXTRACT(HOUR FROM CAST(sbtxdatetime AS TIMESTAMP))
    ) * 60 + EXTRACT(MINUTE FROM CAST('2023-04-03 13:16:30' AS TIMESTAMP)) - EXTRACT(MINUTE FROM CAST(sbtxdatetime AS TIMESTAMP))
  ) * 60 + EXTRACT(SECOND FROM CAST('2023-04-03 13:16:30' AS TIMESTAMP)) - EXTRACT(SECOND FROM CAST(sbtxdatetime AS TIMESTAMP)) AS seconds_diff
FROM main.sbtransaction
WHERE
  EXTRACT(YEAR FROM CAST(sbtxdatetime AS TIMESTAMP)) < 2025
ORDER BY
  4
LIMIT 30
