"""
This files contains the main class Benchmarker. This class is reponsible for
executing the specified tests and collect all metrics.
"""

import os
import time
from datetime import date
from pathlib import Path

import pandas as pd
import psycopg2
from pandas import DataFrame

import pydough
from pydough.unqualified.unqualified_node import UnqualifiedNode
from tests.testing_utilities import harmonize_types


class Connection:
    def __init__(
        self,
        db_name: str,
        user: str,
        password: str,
        host: str = "localhost",
        port: int = 5432,
    ):
        self._conn: psycopg2.extensions.connection = psycopg2.connect(
            database=db_name, user=user, password=password, host=host, port=port
        )

    @property
    def conn(self) -> psycopg2.extensions.connection:
        return self._conn


class Benchmarker:
    """
    Main class responsible for executing the benchmark and collecting all
    the metrics generating the metrics file with the results.
    """

    def __init__(
        self,
        postgres_conn: Connection,
        questions_path: str = "./",
        filename: str = "questions",
        export_metrics: bool = False,
        metadata_path: str = "./benchmark_metadata",
    ) -> None:
        self._connection = postgres_conn
        self._question_metrics = pd.DataFrame(
            {col: pd.Series(dtype=dtype) for col, dtype in self.METRICS_COLUMNS.items()}
        )
        self._benchmark_metrics: dict[str, float] = {}
        self._export_metrics = export_metrics
        self._questions_path = questions_path
        self._filename = filename
        self._metadata_path = metadata_path
        self._run_label = self.get_run_label()

    METRICS_COLUMNS: dict[str, str] = {
        "index": "int",
        "benchmark": "str",
        "question_id": "str",
        "ground_truth": "str",
        "pydough_code": "str",
        "pydough_sql": "str",
        "exec_time": "float",
        "pydough_exec_time": "float",
        "exec_plan": "str",
        "pydough_exec_plan": "str",
        "status": "str",
    }

    """
    Columns for `question_metrics` dataframe:
        index (int): Number for the row, starting on 1
        benchmark (str): Name of the used benchmark
        question_id (str): Unique identifier for the benchmark question.
        ground_truth (str): The reference SQL query considered the correct answer.
        pydough_sql (str): The SQL query generated by PyDough.
        exec_time (float): Execution duration of the ground truth SQL in seconds.
        pydough_exec_time (float): Execution duration of the PyDough SQL in seconds.
        exec_plan (str): The database execution plan for the ground truth query.
        pydough_exec_plan (str): The database execution plan for the PyDough query.
        status (str): The outcome of the comparison (e.g., 'SUCCESS', 'FAILURE', 'TIMEOUT').
    """

    @property
    def connection(self) -> Connection:
        """Independent connection to the database"""
        return self._connection

    @connection.setter
    def connection(self, new_conn: Connection) -> None:
        """
        Sets a new connection
        """
        self._connection = new_conn

    @property
    def question_metrics(self) -> DataFrame:
        """
        A collection of performance and execution metadata comparing PyDough against
        ground truth benchmarks.
        """
        return self._question_metrics

    @question_metrics.setter
    def question_metrics(self, new_question_metrics: DataFrame) -> None:
        """
        Sets a new question_metrics
        """
        self._question_metrics = new_question_metrics

    @property
    def benchmark_metrics(self) -> dict[str, float]:
        """
        General metrics of the benchmark
        """
        return self._benchmark_metrics

    @property
    def questions_path(self) -> str:
        """
        Path of the csv file with all the benchmark questions
        """
        return self._questions_path

    @property
    def filename(self) -> str:
        """
        Name of the file with all benchmark questions without the extension.
        The extension must be `.csv` implicitly
        """
        return self._filename

    @property
    def metadata_path(self) -> str:
        """
        Path of the metadata graph to load in PyDough
        """
        return f"{self._metadata_path}.json"

    @property
    def run_label(self) -> str:
        """
        Label for the benchmark run
        """
        return self._run_label

    def get_run_label(self) -> str:
        """
        Generate the label for this run based on the current date and
        environment variables `RUN_TYPE` and `RUN_ID`.

        RUN_TYPE can be:
        - "R" for release runs (e.g., triggered by a new PyDough release)
        - "M" for manual runs (e.g., triggered by a developer for testing purposes)

        Returns:
            The label for this run, in the format {date}_{run_type}_{run_id}
        """
        run_type: str = os.getenv("RUN_TYPE", "M")
        run_id: str = os.getenv("RUN_ID", str(int(time.time())))

        today = date.today().strftime("%Y%m%d")

        if run_type == "R":
            # e.g. 20260508_R_v1.2.0
            return f"{today}_R_{run_id}"
        else:
            # e.g. 20260508_M_25526200301
            return f"{today}_M_{run_id}"

    def get_questions_df(self) -> DataFrame:
        """
        Get the dataframe with all the benchmark questions required for the run.

        The csv file must be in the format:
        | benchmark | question_id | question | pydough | postgres_sql |
        |-----------|-------------|----------|---------|--------------|
        | TPCH      | Q1          | BS       |pyd.CAL()| SELECT...    |

        Returns:
            DataFrame with all the questions
        """
        full_path_file: str = f"{self.questions_path}/{self.filename}.csv"
        if os.path.isfile(full_path_file):
            return pd.read_csv(full_path_file)

        raise ValueError(f"File {full_path_file} doesn't exists")

    def execute_pydough(self, pydough_str: str) -> tuple[DataFrame, str]:
        """
        Executes the given pyodugh code using `from_string` api and then `to_df` to get the result as a dataframe.

        Args:
            pydough_str: PyDough code to execute

        Returns:
            A tuple with the result of the PyDough code as a DataFrame and the
            SQL generated by PyDough
        """
        try:
            pydough_code: UnqualifiedNode = pydough.from_string(pydough_str)
            result: DataFrame = pydough.to_df(pydough_code)
            return result, pydough.to_sql(pydough_code)
        except Exception as e:
            raise ValueError(
                f"An error occurred while generating the PyDough code: {e}"
            )

    def execute_sql(self, query: str) -> DataFrame:
        """
        Executes the given sql in the established connection

        Args:
            query: SQL query to execute

        Returns:
            DataFrame with the results of the query

        Raises:
            ValueError: If there is an error executing the query
        """
        try:
            with self.connection.conn.cursor() as cur:
                cur.execute(query)
                column_names: list[str] = [
                    description[0] for description in cur.description
                ]

                data = cur.fetchall()
                return pd.DataFrame(data, columns=column_names)

        except (Exception, psycopg2.DatabaseError) as error:
            raise ValueError(f"An error occurred: {error}") from error

    def generate_execute_plan(self, query: str) -> str:
        """
        Executes the instruction to generate the execute plan for the given query

        Args:
            query: SQL query to generate the execute plan

        Returns:
            The execute plan for the given query

        Raises:
            ValueError: If there is an error generating the execute plan
        """

        try:
            with self.connection.conn.cursor() as cur:
                cur.execute(f"EXPLAIN ANALYZE {query}")
                plan = cur.fetchall()
                return "\n".join([row[0] for row in plan])

        except (Exception, psycopg2.DatabaseError) as error:
            raise ValueError(
                f"An error occurred while generating the execution plan: {error}"
            ) from error

    def result_validation(
        self, pydough_result: DataFrame, ground_truth_result: DataFrame
    ) -> None:
        """
        Validates the result of PyDough against the ground truth SQL result.

        It checks that the number of columns is the same, that the column names
        are the same and that the values are the same with a certain tolerance
        for numerical values.

        Args:
            pydough_result: Result of the PyDough code execution as a DataFrame
            ground_truth_result: Result of the ground truth SQL execution as a DataFrame

        Raises:
            AssertionError: If the results are different
        """
        assert len(pydough_result.columns) == len(ground_truth_result.columns), (
            "Number of columns in the result is different between PyDough and the ground truth SQL"
        )
        assert set(pydough_result.columns) == set(ground_truth_result.columns), (
            "Column names in the result are different between PyDough and the ground truth SQL"
        )

        for col_name in pydough_result.columns:
            pydough_result[col_name], ground_truth_result[col_name] = harmonize_types(
                pydough_result[col_name], ground_truth_result[col_name]
            )

        pd.testing.assert_frame_equal(
            pydough_result, ground_truth_result, rtol=1e-6, atol=1e-9, check_like=True
        )

    def generate_metrics_file(self) -> None:
        """
        Generates a csv file with all the results using `run_label` as the name
        of the file.

        The file will be generated in the `benchmark/metrics` folder,
        if it doesn't exist it will be created.

        """

        if not Path("benchmark/metrics/").exists():
            Path("benchmark/metrics").mkdir()

        self.question_metrics.to_csv(f"benchmark/metrics/{self.run_label}.csv")

    def generate_benchmark_metrics(self, total_time_min: float) -> None:
        """
        Generates general metrics for the benchmark (AVG, Desviation, MIN, MAX)

        Args:
            total_time_min: Total time of the benchmark in minutes
        """
        previous_run_label: str | None = None

        new_row = {
            "run": self.run_label,
            "total_questions": len(self.question_metrics),
            "total_time_min": round(total_time_min, 2),
            "pyd_avg_time": round(self.question_metrics["pydough_exec_time"].mean(), 2),
            "pyd_std_time": round(self.question_metrics["pydough_exec_time"].std(), 2),
            "pyd_min_time": round(self.question_metrics["pydough_exec_time"].min(), 2),
            "pyd_max_time": round(self.question_metrics["pydough_exec_time"].max(), 2),
        }

        new_df = pd.DataFrame([new_row])
        combined_df = new_df

        if Path("benchmark/benchmark_metrics.csv").exists():
            existing_df = pd.read_csv("benchmark/benchmark_metrics.csv")

            if not existing_df.empty:
                previous_run_label = existing_df.iloc[-1]["run"]
                combined_df = pd.concat(
                    [existing_df, new_df],
                    ignore_index=True,
                    sort=False,
                )

        combined_df.to_csv("benchmark/benchmark_metrics.csv", index=False)

        self.generate_comparison_file(previous_run_label)

    def generate_comparison_file(self, previous_run_label: str | None) -> None:
        """
        Generates a comparison file (csv file) between the current run and the previous one
        if there is any.

        Args:
            `previous_run`: the run_label of the previous run, must match the
            name of the file on benchmark/metrics folder.

        """

        if (
            not previous_run_label
            or not Path(f"benchmark/metrics/{previous_run_label}.csv").exists()
        ):
            # Not doing comparison
            return

        current_run: DataFrame = pd.read_csv(f"benchmark/metrics/{self.run_label}.csv")
        previous_run: DataFrame = pd.read_csv(
            f"benchmark/metrics/{previous_run_label}.csv"
        )

        # Making sure that each row have unique benchmark-question_id combination
        assert not current_run.duplicated(["benchmark", "question_id"]).any()
        assert not previous_run.duplicated(["benchmark", "question_id"]).any()

        # Comparing results
        merged_df = current_run.merge(
            previous_run,
            on=["index", "benchmark", "question_id"],
            suffixes=("_current", "_previous"),
            how="outer",
        )

        merged_df["improvement_pct"] = (
            (
                merged_df["pydough_exec_time_previous"]
                - merged_df["pydough_exec_time_current"]
            )
            / merged_df["pydough_exec_time_previous"]
        ) * 100

        def status(row):
            if pd.isna(row["pydough_exec_time_previous"]):
                return "new"
            if pd.isna(row["pydough_exec_time_current"]):
                return "removed"
            if row["improvement_pct"] > 0:
                return "improved"
            if row["improvement_pct"] < 0:
                return "regressed"
            return "no_change"

        merged_df["status"] = merged_df.apply(status, axis=1)

        merged_df["improvement_pct"] = merged_df["improvement_pct"].map(
            lambda x: f"{round(x, 3)}%" if pd.notna(x) else ""
        )

        comparison: DataFrame = merged_df[
            [
                "index",
                "benchmark",
                "question_id",
                "pydough_exec_time_previous",
                "pydough_exec_time_current",
                "improvement_pct",
                "status",
            ]
        ]

        comparison = comparison.sort_values(
            by="index",
            na_position="last",
        )

        if not Path("benchmark/comparisons/").exists():
            Path("benchmark/comparisons").mkdir()

        output_path = f"benchmark/comparisons/comparison_{self.run_label}_vs_{previous_run_label}.csv"
        comparison.to_csv(output_path, index=False)

    def measure(self) -> None:
        """
        Main method, executes all necessary steps for the performance measurement
        """
        start_measure: float = time.perf_counter()
        questions: DataFrame = self.get_questions_df()

        # Setup pydough connection
        pydough.active_session.connect_database(
            "postgres", connection=self.connection.conn
        )

        current_graph: str = ""

        for index, row in questions.iterrows():
            print(f"Executing question {index + 1}/{len(questions)}")

            if len(current_graph) == 0 or current_graph != row["benchmark"]:
                current_graph = row["benchmark"]
                pydough.active_session.load_metadata_graph(
                    self.metadata_path, current_graph
                )

            postgres_sql: str = row["postgres_sql"]

            row_metrics: dict[str, str | int | float]

            try:
                # Ground truth metrics
                start = time.perf_counter()
                ground_truth_result: DataFrame = self.execute_sql(postgres_sql)
                elapsed_exec_time = time.perf_counter() - start

                execution_plan: str = self.generate_execute_plan(postgres_sql)

                # PyDough metrics

                start_pyd = time.perf_counter()
                pydough_result, pydough_sql = self.execute_pydough(row["pydough_code"])
                elapsed_pydough_time = time.perf_counter() - start_pyd

                pyd_execution_plan: str = self.generate_execute_plan(pydough_sql)

                # Check for correctness
                self.result_validation(pydough_result, ground_truth_result)

                row_metrics = {
                    "index": index + 1,
                    "benchmark": row["benchmark"],
                    "question_id": row["question_id"],
                    "ground_truth": postgres_sql,
                    "pydough_code": row["pydough_code"],
                    "pydough_sql": pydough_sql,
                    "exec_time": elapsed_exec_time,
                    "pydough_exec_time": elapsed_pydough_time,
                    "exec_plan": execution_plan,
                    "pydough_exec_plan": pyd_execution_plan,
                    "status": "SUCCESS",
                }
            except AssertionError as e:
                # The test failed because the results are different,
                # we collect the metrics but we mark the status as failure
                row_metrics = {
                    "index": index + 1,
                    "benchmark": row["benchmark"],
                    "question_id": row["question_id"],
                    "ground_truth": postgres_sql,
                    "pydough_code": row["pydough_code"],
                    "pydough_sql": pydough_sql,
                    "exec_time": elapsed_exec_time,
                    "pydough_exec_time": elapsed_pydough_time,
                    "exec_plan": execution_plan,
                    "pydough_exec_plan": pyd_execution_plan,
                    "status": f"FAILURE: Wrong answer - {e}",
                }
            except Exception as e:
                # Somethind went wrong during the execution of the queries,
                # we collect all the metrics we can but we mark the status as
                # failure with the error message
                row_metrics = {
                    "index": index + 1,
                    "benchmark": row["benchmark"],
                    "question_id": row["question_id"],
                    "ground_truth": postgres_sql,
                    "pydough_code": row["pydough_code"],
                    "pydough_sql": pydough_sql if "pydough_sql" in locals() else "",
                    "exec_time": elapsed_exec_time
                    if "elapsed_exec_time" in locals()
                    else 0,
                    "pydough_exec_time": elapsed_pydough_time
                    if "elapsed_pydough_time" in locals()
                    else 0,
                    "exec_plan": execution_plan if "execution_plan" in locals() else "",
                    "pydough_exec_plan": pyd_execution_plan
                    if "pyd_execution_plan" in locals()
                    else "",
                    "status": f"FAILURE: {e}",
                }

            self.question_metrics = pd.concat(
                [self.question_metrics, pd.DataFrame([row_metrics])], ignore_index=True
            )

        if self._export_metrics:
            self.generate_metrics_file()
            self.generate_benchmark_metrics((time.perf_counter() - start_measure) / 60)

        # clossing the connection
        self.connection.conn.close()
