SELECT
  sbtxdatetime AS x,
  CAST('2025-05-02 11:00:00' AS TIMESTAMP) AS y1,
  CAST('2023-04-03 13:16:30' AS TIMESTAMP) AS y,
  DATE_DIFF('YEAR', CAST(sbtxdatetime AS TIMESTAMP), CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS years_diff,
  DATE_DIFF('MONTH', CAST(sbtxdatetime AS TIMESTAMP), CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS months_diff,
  DATE_DIFF('DAY', CAST(sbtxdatetime AS TIMESTAMP), CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS days_diff,
  DATE_DIFF('HOUR', CAST(sbtxdatetime AS TIMESTAMP), CAST('2025-05-02 11:00:00' AS TIMESTAMP)) AS hours_diff,
  DATE_DIFF('MINUTE', CAST(sbtxdatetime AS TIMESTAMP), CAST('2023-04-03 13:16:30' AS TIMESTAMP)) AS minutes_diff,
  DATE_DIFF('SECOND', CAST(sbtxdatetime AS TIMESTAMP), CAST('2023-04-03 13:16:30' AS TIMESTAMP)) AS seconds_diff
FROM main.sbtransaction
WHERE
  EXTRACT(YEAR FROM CAST(sbtxdatetime AS TIMESTAMP)) < 2025
ORDER BY
  4 NULLS FIRST
LIMIT 30
