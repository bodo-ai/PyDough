CREATE TABLE SEASONS (
  s_name varchar(7) NOT NULL,
  s_month1 INTEGER,
  s_month2 INTEGER,
  s_month3 INTEGER
);

CREATE TABLE TIMES (
  t_name varchar(10) NOT NULL,
  t_start_hour INTEGER,
  t_end_hour INTEGER
);

CREATE TABLE ERAS (
  er_name varchar(11) NOT NULL,
  er_start_year INTEGER,
  er_end_year INTEGER
);

CREATE TABLE EVENTS(
  ev_key INTEGER,
  ev_name varchar (100) NOT NULL,
  ev_dt datetime TIMESTAMP,
  ev_typ varchar(10)
);

