SELECT
  USERS.SIGNUP_DATE AS signup_date,
  USERS.USER_ID AS user_id
FROM (VALUES
  (TO_TIMESTAMP('2024-01-10 00:00:00', 'YYYY-MM-DD HH24:MI:SS'), 1),
  (TO_TIMESTAMP('2024-01-12 00:00:00', 'YYYY-MM-DD HH24:MI:SS'), 2),
  (TO_TIMESTAMP('2024-02-01 00:00:00', 'YYYY-MM-DD HH24:MI:SS'), 3),
  (TO_TIMESTAMP('2024-02-01 00:00:00', 'YYYY-MM-DD HH24:MI:SS'), 4)) AS USERS(SIGNUP_DATE, USER_ID)
