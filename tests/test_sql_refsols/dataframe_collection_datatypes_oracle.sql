  COLUMN1 AS string_col,
  COLUMN2 AS int_col,
  COLUMN3 AS float_col,
  COLUMN4 AS nullable_int_col,
  COLUMN5 AS bool_col,
  COLUMN6 AS null_col,
  COLUMN7 AS datetime_col
