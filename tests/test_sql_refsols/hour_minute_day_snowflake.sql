SELECT
  SBTRANSACTION.sbtxid AS transaction_id,
  DATE_PART(HOUR, SBTRANSACTION.sbtxdatetime) AS _expr0,
  DATE_PART(MINUTE, SBTRANSACTION.sbtxdatetime) AS _expr1,
  DATE_PART(SECOND, SBTRANSACTION.sbtxdatetime) AS _expr2
FROM MAIN.SBTRANSACTION AS SBTRANSACTION
JOIN MAIN.SBTICKER AS SBTICKER
  ON SBTICKER.sbtickerid = SBTRANSACTION.sbtxtickerid
  AND SBTICKER.sbtickersymbol IN ('AAPL', 'GOOGL', 'NFLX')
ORDER BY
  TRANSACTION_ID NULLS FIRST
